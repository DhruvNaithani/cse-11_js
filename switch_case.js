document.write("switch case <br>");
var grade='A';
switch(grade){
    case 'A':
    document.write("excellent");
    break;
    case 'B':
    document.write("good");
    break;
    case 'C':
    document.write("just pass:");
    break;
    case 'D':
    document.write("study hard");
    break;
    case 'E':
    document.write("study very hard");
    break;
    default:
    document.write("FAIL");
}
